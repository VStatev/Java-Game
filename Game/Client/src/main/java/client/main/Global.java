package client.main;

public class Global {
	public static int maxY = 4;
	public static int maxX = 8;
	/**
	 * @param newmaxY setting the maximal value of the Y coordinate
	 */
	public static void setMaxY(int newmaxY) {
		maxY = newmaxY;
	}
	/**
	 * @param newMaxX setting the maximal value of the X coordinate
	 */
	public static void setMaxX(int newMaxX) {
		maxX = newMaxX;
	}

}
